//Sınırlamasız Sınıf Ortalaması 
#include <iostream>  
#include <locale.h>  
using namespace std;  
int main()  
{  
  setlocale(LC_ALL,"Turkish");  
  double toplam,notSayaci,sinavNotu,ortalama;  
  toplam=0;  
  notSayaci=0;  

  while(sinavNotu !=-1)  
  {  
  toplam=toplam+sinavNotu;
  
  cout<<"\nUYARI:Not girme işlemi bittiğinde -1 değerini giriniz.\nÖğrencinin aldığı puanı girin:";  
  cin>>sinavNotu;  
  
  notSayaci=notSayaci+1;  
  }  
  ortalama=toplam/(notSayaci-1);  

  cout<<"\n\n\tSınıf Ortalaması: "<<ortalama<<endl;  

return 0;  
}  
