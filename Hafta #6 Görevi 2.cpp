//Alt-Üst limit ile sayıları göster ve toplamı belirle 
#include <iostream> 
#include <locale.h> 
using namespace std; 
int main() 
{ 
	setlocale(LC_ALL,"Turkish"); 
	int toplam,baslangic,bitis; 

	cout<<"Alt limiti giriniz : "; 
	cin>>baslangic; 

	cout<<"Üst limiti giriniz : "; 
	cin>>bitis;
  	
	for(int x=baslangic; x<=bitis; x++)
{
	toplam=x+toplam;
	cout<<x<<endl;
}
	cout<<"Sayıların toplamı : "<<toplam<<endl; 

return 0; 
}
